# ----------------- SETS -------------------
# * sets are unordered collection of data items
# * They are store multiple items in a single variable and enclosed with a {} curly brackets
# * sets are immutable, sets do not contain duplicate items

s={2,4,2,6}
print(s)
print(type(s))

info={'Car', 21, 5.68, 21}
print(info)

for value in info:
    print(value)

usman=set()
print(type(usman))