#The Conversion of one data into the other data type is known as typecasting in python or type conversion in python

# ... Explicit TypeCasting ...
string='15'
number=7
string_number=int(string)
sum=number+string_number
print('The Sum of both these number is:', sum)

# ... Implicit TypeCasting ...
a=6.6
b=5
print(a+b)
